    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        
        String caminho = new File("./relatorio/carteirinha.jrxml").getAbsolutePath();
        try {
            JasperReport relatorio = JasperCompileManager.compileReport(caminho);
            int ls = masterTable.getSelectedRow();
            Pessoa pessoa = list.get(ls);
            List<Pessoa> listPessoa = new ArrayList<>();
            listPessoa.add(pessoa);         
            JRBeanCollectionDataSource dados = new JRBeanCollectionDataSource(listPessoa,false);            
            JasperPrint print = JasperFillManager.fillReport(relatorio, null, dados);
            JasperViewer view = new JasperViewer(print, false);
            view.setVisible(true);
            
        } catch (JRException ex) {
            Logger.getLogger(JFrmCadPessoa.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton7ActionPerformed

