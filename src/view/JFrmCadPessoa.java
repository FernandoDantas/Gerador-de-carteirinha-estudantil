    private void atualizaFoto(){
        int ls = masterTable.getSelectedRow();
        if(ls >= 0){
            Pessoa p = list.get(ls);
            String nomeFoto = p.getFoto();
            
            if(!nomeFoto.isEmpty()){
                
                ImageIcon icon = new ImageIcon("./fotos/"+nomeFoto);
                jLabel2.setIcon(icon);
            }            
        }
    }
    
