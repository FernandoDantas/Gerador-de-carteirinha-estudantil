    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        List<Pessoa> pessoas = new ArrayList<>();
        
        pessoas.addAll(jList1.getSelectedValuesList());
        
        String caminho = new File("./relatorio/etiquetas.jrxml").getAbsolutePath();
        try {
            JasperReport relatorio = JasperCompileManager.compileReport(caminho);                                
            JRBeanCollectionDataSource dados = new JRBeanCollectionDataSource(pessoas,false);            
            JasperPrint print = JasperFillManager.fillReport(relatorio, null, dados);
            JasperViewer view = new JasperViewer(print, false);
            view.setVisible(true);
            
        } catch (JRException ex) {
            Logger.getLogger(JFrmCadPessoa.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

